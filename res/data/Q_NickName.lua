Q_NickName.GetTempData = function (key, strName)
                             if not Q_NickName[key] or not Q_NickName_index[strName] or type(key) == "function" then
                                    log_info("Q_NickName.log"," the data is nil or the index data is nil or key is function key =%s , strName = %s" , tostring(key) , tostring( strName))
