Q_Shop.GetTempData = function (key, strName)
                             if not Q_Shop[key] or not Q_Shop_index[strName] or type(key) == "function" then
                                    log_info("Q_Shop.log"," the data is nil or the index data is nil or key is function key =%s , strName = %s" , tostring(key) , tostring( strName))
                                    return nil
                             end
                             if type(Q_Shop[key]) ~= "table" then
                                    log_info("Q_Shop.log"," the data is not table key =%s , strName = %s" , tostring(key) , tostring( strName))
                                return nil
                             end
                             local pData = Q_Shop[key][Q_Shop_index[strName]]
                             if not pData then
                                 log_info("Q_Shop.log"," the data is nil key =%s , strName = %s" , tostring(key) , tostring( strName))
                                 return nil
                             end
                             return pData
                         end
